  <section class="fv-card section">
    <h2>The short version</h2>
    <p class="fv-muted">Choose Jobber if you want the most polished mobile experience in field service, consumer-facing online booking, and a huge ecosystem of templates, content, and integrations. Choose FullVue if your operation is more than dispatch-and-invoice: real inventory and purchasing, counter sales, equipment and rentals, and a staff time clock with scheduling — priced by team size instead of per-seat licenses and add-on stacking.</p>
  </section>

  <section class="fv-card section">
    <h2>Where Jobber is genuinely better</h2>
    <p class="fv-muted">We're not going to pretend otherwise:</p>
    <ul>
      <li>Mobile apps. Jobber's iOS and Android apps have years of releases and refinement behind them. FullVue runs fully in any phone browser and has an Android app — but no iOS app today.</li>
      <li>Consumer-facing booking. Jobber's online booking and request forms are built for homeowners finding you on Google. FullVue's customer portal is built for repeat commercial customers, not one-off consumer bookings.</li>
      <li>Track record and reviews. Jobber has years of public reviews and a large community. FullVue is newer, with a short public track record.</li>
      <li>Ecosystem. Jobber's template library, content, and integration marketplace are enormous. Ours is small.</li>
    </ul>
  </section>

  <section class="fv-card section">
    <h2>Where FullVue is built differently</h2>
    <p class="fv-muted">FullVue covers ground Jobber wasn't designed for:</p>
    <ul>
      <li>Purchasing and real inventory. Purchase orders, receipts, and bills linked on one screen; a low-stock dashboard widget and a purchase-request queue so reordering doesn't live in someone's head.</li>
      <li>Counter sales. A POS screen with barcode scan-to-cart, for shops that sell parts or product over the counter alongside job work.</li>
      <li>Assets and rentals. Track your own equipment, or rent to customers — with online self-service reservations and damage charges that flow straight onto the invoice.</li>
      <li>Attendance with scheduling. A time clock plus a staff scheduling suite — week rosters, shift swaps, availability, PTO — producing payroll-ready reports your bookkeeper or payroll provider runs with. (Not payroll processing; we don't move money or file taxes.)</li>
      <li>E-sign quotes included. Customers sign from a link on their phone — no login, no DocuSign subscription — and a signed quote can create the job automatically.</li>
      <li>Customer portal and two-way SMS. Customers see their jobs, quotes, invoices, and rentals, and you can text them with opt-in consent handled for you.</li>
    </ul>
  </section>

  <section class="fv-card section">
    <h2>Pricing, side by side</h2>
    <p class="fv-muted">Jobber's published pricing runs $39 (Core), $119 (Connect), and $199 (Grow) per month for solo plans, with team tiers from $169 to $599 per month plus $29 per month for each additional user — and several important capabilities are sold as add-ons, commonly $79–$99 per month each. That model works, but the bill grows every time you hire and every time you turn something on.</p>
    <p class="fv-muted">FullVue prices by team size in five-employee brackets. A 10-person shop running the Field Service bundle is about $242 per month billed annually (monthly billing runs about 12.5% more); the entire suite — every module we make — is about $433 per month. Hiring one tech inside your bracket doesn't add a line item, and the modules you pick are the whole bill. Base prices are published on our pricing page.</p>
  </section>

  <section class="fv-card section">
    <h2>Who should pick which</h2>
    <p class="fv-muted">Pick Jobber if you're a residential service business that wants best-in-class mobile polish, online booking, and a proven ecosystem. Pick FullVue if your shop bills time and materials and needs inventory, purchasing, counter sales, or equipment tracking in the same system as jobs and invoices — and you'd rather pay by team size than watch per-seat and add-on charges stack up.</p>
    <p class="fv-muted">One more difference worth naming plainly: FullVue is built by a veteran-owned company in Appleton, Wisconsin, with a first customer running it since 2023. Support is answered by the people who built it — replies within one business day. If you have a question this page doesn't answer, ask us — and if Jobber really is the better fit for your shop, we'll say so.</p>
  </section>

