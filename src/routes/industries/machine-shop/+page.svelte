    <h2>Where job shops leak margin</h2>
    <p class="fv-muted">Talk to owners on the machining forums and the same four leaks come up, in almost the same words. Fabrication job costing isn't a report you run in April — it's plugging these while the job is still on the floor.</p>
    <ul>
      <li>The $20-a-part surprise. You book a month of work, ship it, and find out at the end you were effectively taping a twenty to every part that went out the door. Quote-vs-actuals on each job shows the drift while the run is live — when you can still fix the price on the next release.</li>
      <li>The handwritten-traveler tax. Somebody spends evenings copying paper job tickets into QuickBooks. When the floor logs straight to the job, the invoice is already built — and it pushes to QuickBooks Online instead of being typed twice.</li>
      <li>The two-hour time-card morning. Reconciling punches by hand costs someone half a morning, every morning. Attendance is a time clock plus staff scheduling that produces payroll-ready reports your bookkeeper or payroll provider runs with. It doesn't process payroll — and we won't tell you it does.</li>
      <li>Buying steel you already own. Low-volume, high-mix shops pile up drops and remnants nobody tracked, then buy fresh plate for a job the rack could have covered. Inventory counts tie into jobs, a low-stock widget flags what's actually short, and a purchase-request queue turns reorders into decisions instead of panic buys.</li>
    </ul>
