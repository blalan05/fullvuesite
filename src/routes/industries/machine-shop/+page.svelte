    <h2>One job, front door to final invoice</h2>
    <p class="fv-muted">Here's a job's day in the life inside FullVue — the same loop your shop already runs, minus the re-keying.</p>
    <p class="fv-muted">Quote, signature, job, actuals, invoice. Because every step lands on the same record, "did we make money on that one?" becomes a lookup instead of an argument.</p>
    <ul>
      <li>Quote it with real numbers. Build a structured quote with material, machine time, and labor as their own lines — so the price is built from costs, not from what you charged last time and hoped.</li>
      <li>The customer signs from a link. Send a quote link they open on their phone and e-sign. No customer login. No DocuSign subscription on your end.</li>
      <li>The job creates itself. The moment the quote is signed, FullVue turns it into a job with the tasks and materials carried over. Nobody re-types a traveler.</li>
      <li>The floor logs the work. Operators log time and materials against the job as it moves — setup, run time, the extra plate you had to shear when the first one warped.</li>
      <li>Steel gets bought on the record. Raise a purchase order for the material, receive it, and see the PO, the receipt, and the vendor's bill linked on one screen, tied back to the job.</li>
      <li>You invoice the actuals. Draft the invoice straight from logged job data, adjust lines where reality differed, email the PDF and watch for the read — then send invoices and bills to QuickBooks Online so the books stay current without double entry.</li>
    </ul>
