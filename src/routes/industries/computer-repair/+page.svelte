    <h2>One workflow: bench ticket to counter sale</h2>
    <p class="fv-muted">A machine comes in. You open a bench ticket and attach the customer's asset from the Assets module — the device, its components, the software on it, who it's assigned to. If you've touched it before, the history is right there. One shop owner described exactly what he wanted from his next system: an "asset driven work flow, not ticket or customer driven." That's how FullVue is built.</p>
    <p class="fv-muted">Parts come off your inventory and post to the ticket, so job costing happens while you work instead of at month-end. For bigger work — a fleet refresh, a custom build — quote it first. Your customer signs the quote from a link on their phone: no login for them, no DocuSign subscription for you. The moment they sign, FullVue creates the job with your tasks and materials already on it.</p>
    <p class="fv-muted">While the work runs, customers get updates by text — real two-way SMS, with opt-in consent handled — or check the portal themselves to see jobs, quotes, and invoices. They can even leave a voice message there; you read the transcript with one click.</p>
    <p class="fv-muted">At pickup, they grab a cable and an SSD off the wall. The Counter module scans both into the cart by barcode, from the same inventory the bench pulls from — one customer, one visit, one clean record. Invoice it, and send it to QuickBooks Online.</p>
    <p class="fv-muted">Custom-PC builders: a build is a simple one-level parts list, so the components a build uses come out of the same inventory. No nested manufacturing BOMs to configure — and no pretending you need them.</p>
