      <li>Lost walk-ins over stock. Owners describe losing customers every week because a common part wasn't on hand. Without reorder alerts somebody actually sees, it keeps happening.</li>
      <li>QuickBooks integrations that stop halfway. "Doesn't sync parts, inventory, purchase orders, vendors" is the recurring review. When a sync half-works, your books and your shelves disagree — and you're the referee.</li>
      <li>Ticket-first tools in an asset-first business. You service machines, some for years. Most tools ask "what's the ticket?" when your real question is "what's the history on this machine?"</li>
