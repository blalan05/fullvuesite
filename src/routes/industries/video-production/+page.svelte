    <h2>What FullVue covers</h2>
    <p class="fv-muted">FullVue is operations software. For a production company, that means:</p>
    <ul>
      <li>Gear checkout. Every body, lens, and light gets a record. Check gear out to a crew member for a shoot and back in when it returns; overdue gear shows up on your dashboard. If you also rent gear out, customers can reserve online themselves, and damage charges flow straight onto their invoice.</li>
      <li>Shoots as jobs. Each production is a job with its own tasks, scheduled days, materials, and crew time — planned on a calendar, costed as it happens.</li>
      <li>Quotes clients sign from their phone. Send a link; the client signs it with no login and no DocuSign subscription on either end. When they sign, the job can be created automatically with the tasks carried over.</li>
      <li>Crew time that lands on the project. Crew clock in against real work, so you can see quoted versus actual per shoot — the number that tells you which clients are worth keeping.</li>
      <li>Invoicing without the lag. Invoice from actuals the day you deliver, and send invoices and bills to QuickBooks Online so your books stay where your accountant likes them.</li>
      <li>Crew comms in the same system. Tether is built-in team chat — channels, attachments, voice notes — so shoot-day logistics stop living in six group texts. Clients get a portal for their quotes, jobs, and invoices, and two-way text messaging with opt-in consent handled for you.</li>
